						A.個人の場合<br>
						・運転免許証<br>
						・各種健康保険証<br>
						・住民票の写し<br>
						・日本国パスポート<br>
						・外国人登録証明書／在留カード登録証明書<br>
						・住民基本台帳カード<br>
					</p>
