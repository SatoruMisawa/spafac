					</h2>
					<p class="indm">
						1.	スペース利用者が自己都合により予約を変更あるいはキャンセルする場合、甲は別表に定めた料率に従い、スペース利用者にキャンセル料を請求するものとする。
					</p>
					<p class="indm">
						2.	スペース利用者が、降雨・降雪その他悪天候を理由にスペースを利用しない場合、屋外のスペースに限ってキャンセルできるものとする。スペース利用者から悪天候を理由にスペース利用のキャンセル申請があった場合、乙は登録スペース周辺の天候を確認のうえ、キャンセル可否の回答をするものとする。なお、スペース利用者によるキャンセルはスペース利用日の前日から翌日まで受け付けるものとする。乙によるキャンセル承認後、スペース利用料の全額はスペース利用者に返金されるものとする。ただし、甲との事前協議の上、スペースごとに降雨・降雪時もスペースのキャンセルを不可とすることもできるものとする。
