					<p class="in">
						Space Factory（スペースファクトリー）（以下「甲」という）は、甲の運営するインターネット上の情報提供サービス（以下「当サービス」という）へのスペース登録を引き受ける場合に、以下の規約（以下「本規約」という）に定められた条件によって、引き受けるものとする。甲およびスペース登録する者（以下「乙」という）の責任および権利は本規約に記載のとおりとする。乙は、本規約記載の各条項に承諾してスペース登録することに同意する。
