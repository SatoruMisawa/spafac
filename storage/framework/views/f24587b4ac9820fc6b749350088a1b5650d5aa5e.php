						第 1 条（適用範囲および定義）
					</h2>
					<p class="indm">
						1.	当サービスの適用範囲は、日本国内のスペースに限定するものとする。ただし乙または利用者の居住地については、予約申し込みの完了、支払が正しく完了することが保全されていることを条件に、この限りではないものとする。
					</p>
					<p class="indm">
						2.	本規約において以下の用語は、それぞれに規定するとおりとする。
					</p>
					<p class="in">
						「スペース利用者募集」
					</p>
					<p class="in">
						当サービスを介して提供される次の業務の総称をいう。
					</p>
					<p class="indm">
						A.スペース利用者の集客
					</p>
					<p class="indm">
						B.スペースの予約受付
					</p>
					<p class="indm">
						C.スペースの利用における審査
					</p>
					<p class="indm">
						D.「スペース利用規約」の使用
					</p>
					<p class="indm">
						E.スペース利用料の集金
					</p>
					<p class="in">
						「スペース記載のページ」
					</p>
					<p class="in">
						当サービス内のスペースページをいう。
					</p>
					<p class="in">
						「アカウント/ダッシュボード」
					</p>
					<p class="in">
						乙の会員登録情報および貸し出しするスペースの登録情報の確認・変更ができる乙専用のページをいう。
					</p>
