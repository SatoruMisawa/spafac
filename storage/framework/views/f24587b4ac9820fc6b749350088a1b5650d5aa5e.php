					</p>
					<p class="indm">
						4.	甲は、前項におけるスペースページ・マイページの枠組み、データベースシステムおよびソフトウェアについて、甲の判断により自由にその仕様を改正や変更、バージョンアップをすることができる。
					</p>
					<h2 class="ordinance">
						第 3 条（コンテンツの表示）
					</h2>
					<p class="indm">
						1.	甲は、前項により乙の制作したコンテンツの確認を甲の定める審査基準により行うものとする。当該コンテンツが、甲の審査基準を満たしていると判断した場合のみ、甲は、当該コンテンツを利用したスペース登録を許諾するものとし、その旨を乙に電子メールにて通知するととも
						に、当該スペースページをWEBサイト上に公開する。乙は当該通知を受領したときから、当該スペースページを利用して利用者募集を行うことができる。
					</p>
					<p class="indm">
						2.	乙は、本規約等により認められる範囲内で、スペースページ上のコンテンツを改訂し、表示することができる。乙は、コンテンツについては常に最新の情報をユーザに提供するよう定期的に更新を行う。
					</p>
					<p class="indm">
						3.	甲は、乙の作成したコンテンツが本サービスにふさわしくないと判断した場合には、その内容および表示を変更するよう求めることができ、乙はこれに従うものとする。
					</p>
					<p class="indm">
						4.	スペースページに登録可能なスペースは1つとする。乙が複数のスペースを登録希望の場合は、必要数に応じたスペース登録を行うものとする。
					</p>
					<h2 class="ordinance">
						第 4 条（システム利用料と保険事務手数料）
					</h2>
					<p class="indm">
						1.	甲は、乙が設定した１利用回分のスペース利用料に保険事務手数料（１利用あたり３００円）を上乗せした料金でスペース利用者に請求するものとする。本項は、広告での利用の場合は、適用外とするものとする。
					</p>
					<p class="indm">
						2.	乙は、甲に対し、本規約に基づき乙が利用する甲のデータベースシステムの利用料（以下「システム利用料」という）として、本条に基づき算出されるスペースページにおけるスペース登録者別合計金額から保険事務手数料に月間の確定予約回数を乗じた金額を差し引いた金額（以下「月額スペース利用料」という）に下記システム料率（消費税別）を乗じた金額の合計額を支払う。
