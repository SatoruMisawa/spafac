						第 6 条（利用審査）
					</h2>
					<p class="indm">
						1.	乙のスペースページより予約が入った場合、乙が設定する２日～7日の期間内にマイページ内から利用の可否を利用者に通知するものとする。乙が期限内に回答を行わなかった場合、予約は自動的にキャンセルされるものとする。
					</p>
					<h2 class="ordinance">
						第 7 条（集金代行）
					</h2>
					<p class="indm">
						1.	甲は、乙の代理としてスペース利用者からの集金をする。
					</p>
					<p class="indm">
						A.甲は、甲指定の銀行口座へスペース利用者からの指定口座への振込またはクレジットカード決済をもって集金するものとする。
					</p>
					<p class="indm">
						B.スペース利用者からの集金をもって、予約は確定するものとする。
					</p>
					<p class="indm">
						C.甲は、スペース利用者から集金したスペース利用料を締め日で集計し、収支報告書を乙に通知報告するものとする。その内容に異議のある場合には、甲に対し、１週間以内にこれを通知しなければならない。乙がこの通知をせず、１週間が経過した場合には、収支は確定する。
