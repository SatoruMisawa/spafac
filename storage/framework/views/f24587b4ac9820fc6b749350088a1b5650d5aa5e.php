						第 5 条（利用者募集）
					</h2>
					<p class="indm">
						1.	乙は、当サービスを介してスペース利用者にスペースの貸し出しをするに際し、当該取引の当事者は乙とスペース利用者であることを自覚したうえで、利用者募集を行うものとする。
					</p>
					<p class="indm">
						2.	乙は、利用者募集を行うにあたり、不当表示防止法、その他関係法令を遵守する。
					</p>
