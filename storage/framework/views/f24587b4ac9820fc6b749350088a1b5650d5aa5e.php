						「スペース利用者」
					</p>
					<p class="in">
						当サービスを利用したスペースの利用者をいう。
					</p>
					<p class="in">
						「月間のスペース利用料」
					</p>
					<p class="in">
						当月1日から当月末日までの期間内で、実際に現地にて利用された利用料の合計金額をいう。
